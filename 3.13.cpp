#include<iostream>
using namespace std;

int main()
{
	int i;
	float j;
	cout << "请输入您的利润：";
	cin >> i;
	if (i <= 100000)
	{
		j = i * 0.1;
	}
	else if (i > 100000 && i <= 200000)
	{
		j = 100000 * 0.1 + (i - 100000) * 0.075;
	}
	else if (i > 200000 && i <= 400000)
	{
		j = 100000 * 0.1 + 100000 * 0.075 + (i - 200000) * 0.05;
	}
	else if(i > 400000 && i <= 600000)
	{
		j = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 + (i - 400000) * 0.03;
	}
	else if (i > 600000 && i <= 1000000)
	{
		j = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05 
			+ 200000 * 0.03 + (i - 600000) * 0.015;
	}
	else
	{
		j = 100000 * 0.1 + 100000 * 0.075 + 200000 * 0.05
			+ 200000 * 0.03 + 400000 * 0.015 + (i - 1000000)*0.01;
	}
	cout << "您的奖金为：" << j << endl;
	return 0;
}