//给出两个链表形式表示的数字, 
// 写一个函数得到这两个链表相乘乘积。
//
//样例
//样例 1:
//
//输入：9->4->6->null, 8->4->null
//输出：79464
//解释：946 * 84 = 79464
//样例 2 :
//
//	输入：3->2->1->null, 1->2->null
//	输出：3852
//	解释：321 * 12 = 3852


#include<stdio.h>

int main()
{
	int a = 0, b = 0;
	printf("a=");
	scanf("%d", &a);
	printf("b=");
	scanf("%d", &b);
	printf("%d\n", a * b);
	return 0;
}